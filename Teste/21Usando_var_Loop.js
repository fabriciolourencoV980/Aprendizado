// O laço for em JavaScript é uma estrutura de controle usada para repetir um bloco de código um número específico de vezes. Ele é frequentemente utilizado quando você sabe com antecedência quantas vezes deseja executar o bloco de código.

for (var i = 0; i < 10; i++){
    console.log(i); 
};
console.log('i =', i);